export const getSweetSpotById = (id: string): SweetSpot | undefined => {
  return sweetSpots.find(s => s.id === id);
};

export const getSweetSpotsByProgram = (programId: string): SweetSpot[] => {
  return sweetSpots.filter(s => s.programId === programId);
};

export const getSweetSpotsByCabin = (cabinClass: string): SweetSpot[] => {
  return sweetSpots.filter(s => s.cabinClass === cabinClass);
};

export const getTopSweetSpots = (limit: number = 6): SweetSpot[] => {
  return [...sweetSpots].sort((a, b) => b.valueCpp - a.valueCpp).slice(0, limit);
};
